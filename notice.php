    <p><b class="smalltitle" id="repairs">Rights to repairs</b>
    <br><br>There's generally a difference between urgent or emergency repairs and those which are deemed to be non-urgent. Urgent repairs are generally specified as those which pose a danger or which are likely to cause undue inconvenience, such as a dangerous electrical fault or a blocked or broken toilet.
    <span style="display:none;" id="expand-text2">
    <br>
    <br>Beyond these types of repairs, each state and territory – in theory – requires landlords to conduct repairs to maintain the property in a reasonable condition, but in practice this doesn't always happen. In fact, recent research by Consumer Affairs Victoria as part of its review of the state's tenancy legislation found that 53% of tenants had experienced problems in getting repairs completed. In addition, only 40% of tenants who had requested non-urgent repairs reported they had them completed promptly and to an acceptable standard.  
    <br>
    <br>You can't stop paying your rent if your landlord doesn't do the repairs. But you may be entitled to apply to your relevant tenancy tribunal to have your rent paid into a special account until the repairs are done.
    <br>
    <br>So what sort of repairs and maintenance are covered? For non-urgent repairs, the standard of repairs you can expect will differ depending on:
    <br><br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;.1.the state of the property when you moved in
        <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;.2.the age of the property and its prospective life, and
        <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;.3.the price you pay for the property (potentially).  
    <br>
    <br>Excessive mould as a result of structural issues or broken elements on a stove top are common and non-urgent issues where tenants should be entitled to repairs, according to the Tenants' Union of Victoria.
    <br>
    <br>It seems tenants are often too scared to ask for repairs for fear of being kicked out. A survey by the Tenants' Union of NSW in 2014 found 77% of respondents had put up with a problem because they were worried about adverse consequences if they asked to get it fixed.
